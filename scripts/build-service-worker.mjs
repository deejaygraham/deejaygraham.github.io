import * as esbuild from "esbuild";
import { createHash } from "crypto";
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname, normalize } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const SW_DIR = join(ROOT, "src/service-worker");
const OUT_FILE = join(ROOT, "src/_generated/sw.js");

/** JS sources that contribute to the SW bundle, excluding generated cache-version. */
function sourceFilesForHash() {
  return readdirSync(SW_DIR)
    .filter(
      (name) =>
        name.endsWith(".js") &&
        !name.endsWith(".test.js") &&
        name !== "cache-version.js",
    )
    .sort();
}

export function computeCacheVersionSuffix() {
  const hash = createHash("sha256");
  for (const name of sourceFilesForHash()) {
    hash.update(readFileSync(join(SW_DIR, name)));
  }
  return hash.digest("hex").slice(0, 8);
}

export async function buildServiceWorker() {
  const suffix = computeCacheVersionSuffix();
  writeFileSync(
    join(SW_DIR, "cache-version.js"),
    `// Generated by scripts/build-service-worker.mjs — do not edit by hand\nexport const CACHE_VERSION = '-${suffix}';\n`,
  );

  mkdirSync(dirname(OUT_FILE), { recursive: true });

  await esbuild.build({
    entryPoints: [join(SW_DIR, "index.js")],
    bundle: true,
    outfile: OUT_FILE,
    format: "esm",
    platform: "browser",
    target: ["es2022"],
    banner: {
      js:
        '/// <reference lib="WebWorker" />\n',
    },
  });
}

const runCli =
  process.argv[1] &&
  normalize(fileURLToPath(import.meta.url)) === normalize(process.argv[1]);

if (runCli) {
  buildServiceWorker().catch((err) => {
    console.error(err);
    process.exit(1);
  });
}
